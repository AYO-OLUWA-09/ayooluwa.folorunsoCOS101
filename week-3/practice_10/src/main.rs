fn main() {
    // addition
    let sum:f32 = 5_550.00 + 7_310.00;
    println!("The Sum of 5_550 and 7_310 is {}",sum);

    // subtraction
    let difference:f32 = 95.50 - 4.30;
    println!("The difference of 95.50 and 4.30 is {}",difference);


    // multiplication
    let product:f32 = 4.0 * 30.0;
    println!("The multiple of 4.0 and 30.0 is {}",product);


    // division
    let quotient:f32 = 56.70 / 32.20;
    println!("The division of 56.70 and 32.20 is {}",quotient);


    // remainder 
    let remainder:f32 = 43.0 % 5.0;
    println!("The remainder of 43.0 and 5.0 is {}",remainder);


}
